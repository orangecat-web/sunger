<!--測試網址用 , 啟用正式網址前請先修改成 "index,follow"-->
<meta name="robots" content="noindex,nofollow">

<!--搜尋引擎用 尤其是 google-->
<title>上格西服 - 貝爾校稿網站</title>
<!--這個是網站製作者-->
<meta name="author" content="貝爾網站國際有限公司">
<!--這個是網站版權-->
<meta name="copyright" content="上格西服">
<!--這個是連結頁面文章的敘述-->
<meta name="description" content="上格西服">
<!--這個是連結頁面關鍵字-->
<meta name="keyword" content="上格西服">
<!--這個是連結頁面的標題-->
<meta property="og:title" content="上格西服">
<!--這個是連結頁面的網址-->
<meta property="og:url" content=" http://www.example.com/">
<!--這個是分享畫面的圖片路徑 解析須200x200至1500x1500之間，檔案大小在5MB以內-->
<meta property="og:image" content=" http://example.com/image.jpg">
<!--這個是連結頁面文章的敘述-->
<meta property="og:description" content="上格西服">
